#include <stdio.h>
#include <stdlib.h>
int main(){
    int a = 5; //This is an integer
    float b = 5.6; // This is a decimal real no.
    char c = 'halua'; //This is a character
    printf("The value of a is %d \n",a); //%d is used for integer val
    printf("The value of b is %f \n",b); //%f is used for decimal val (float)
    printf("The value of c is %c \n",c); //%c is used for characters
    printf("The value of a+b is %f \n",a+b); //%c is used for characters
    system("pause");
    return 0;
}